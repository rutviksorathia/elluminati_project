                                    width: 30,
                                    child: Center(
                                      child: Text(
                                        '+',
                                      ),
                                    ),
                                  ),
                                ),
                              ],
                            ),
                          ],
                        ),
                      ),
                      const SizedBox(width: 20),
                      Flexible(
                        flex: 1,
                        fit: FlexFit.tight,
                        child: Container(
                          height: 40,
                          decoration: BoxDecoration(
                            borderRadius:
                                const BorderRadius.all(Radius.circular(20)),
                            border: Border.all(
                              width: 1,
                              color: Colors.black,
                            ),
                          ),
                          child: Padding(
                            padding: const EdgeInsets.symmetric(
                                horizontal: 20, vertical: 8),
                            child: Center(
                                child: Text('Add To Cart - ${model.price}')),
                          ),
                        ),
                      ),
                    ],
                  ),
                ),
              )
            ],
          ),
        );
      },
    );
  }
}

class RightCheck extends StatefulWidget {
  List1 list;
  HomeViewModel model;

  RightCheck({
    super.key,
    required this.list,
    required this.model,
  });

  @override
  State<RightCheck> createState() => _RightCheckState();
}

class _RightCheckState extends State<RightCheck> {
  int increasePrice = 1;

  @override
  Widget build(BuildContext context) {
    return Row(
      children: [
        Checkbox(
          value: widget.list.isDefaultSelected,
          onChanged: (value) {
            print(value);
            if (value == null) return;
            widget.list.isDefaultSelected = value;
            widget.model.notifyListeners();
            if (value) {
              setState(() {
                print(widget.list.price);
                widget.model.price = widget.model.price + widget.list.price;
              });
            } else {
              setState(() {
                widget.model.price =
                    widget.model.price - (widget.list.price * increasePrice);
              });
            }
          },
        ),
        Flexible(
          flex: 1,
          fit: FlexFit.tight,
          child: Row(
            children: [
              Flexible(
                flex: 1,
                fit: FlexFit.tight,
                child: Text(
                  widget.list.name.first,
                  overflow: TextOverflow.fade,
                ),
              ),
              if (widget.list.isDefaultSelected && widget.list.price != 0)
                Container(
                  width: 100,
                  height: 25,
                  decoration: BoxDecoration(
                    borderRadius: const BorderRadius.all(
                      Radius.circular(
                        20,
                      ),
                    ),
                    border: Border.all(
                      width: 1,
                      color: Colors.black,
                    ),
                  ),
                  child: Row(
                    children: [
                      Row(
                        children: [
                          GestureDetector(
                            onTap: increasePrice != 1
                                ? () {
                                    setState(() {
                                      increasePrice--;
                                      widget.model.price = widget.model.price -
                                          (widget.list.price);
                                      widget.model.notifyListeners();
                                    });
                                  }
                                : null,
                            child: const SizedBox(
                              width: 30,
                              child: Center(
                                child: Text(
                                  '-',
                                ),
                              ),
                            ),
                          ),
                          Container(
                            width: 1,
                            color: Colors.black,
                          ),
                          SizedBox(
                            width: 30,
                            child: Center(
                              child: Text(
                                increasePrice.toString(),
                              ),
                            ),
                          ),
                          Container(
                            width: 1,
                            color: Colors.black,
                          ),
                          GestureDetector(
                            onTap: () {
                              setState(() {
                                increasePrice++;
                                widget.model.price =
                                    widget.model.price + (widget.list.price);
                                widget.model.notifyListeners();
                                print(widget.model.price);
                              });
                            },
                            child: const SizedBox(
                              width: 30,
                              child: Center(
                                child: Text(
                                  '+',
                                ),
                              ),
                            ),
                          ),
                        ],
                      ),
                    ],
                  ),
                ),
            ],
          ),
        ),
        const SizedBox(width: 20),
        Text(
          '₹ ${widget.list.price.toString()}',
        ),
      ],
    );
  }
}
