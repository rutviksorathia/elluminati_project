class HomeView extends StatelessWidget {
  const HomeView({super.key});

  @override
  Widget build(BuildContext context) {
    return ViewModelBuilder<HomeViewModel>.reactive(
      viewModelBuilder: () => HomeViewModel(),
      onViewModelReady: (model) {
        model.readJson();
      },
      builder: (context, model, _) {
        return Scaffold(
          body: Column(
            children: [
              if (model.shouldShowAppBar)
                Container(
                  height: 80,
                  color: Colors.black,
                ),
              Expanded(
                child: SingleChildScrollView(
                  controller: model.apartmentControl,
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Container(
                        height: 220,
                        color: Colors.grey.shade200,
                        child: SizedBox(
                          width: double.infinity,
                          child: Align(
                            alignment: Alignment.bottomLeft,
                            child: Padding(
                              padding: const EdgeInsets.only(
                                bottom: 6,
                              ),
                              child: Container(
                                height: 70,
                                color: Colors.white,
                                width: double.infinity,
                                child: Padding(
                                  padding:
                                      const EdgeInsets.only(top: 12, left: 18),
                                  child: model.autogenerated != null
                                      ? Text(
                                          model.autogenerated!.name.first,
                                          style: const TextStyle(
                                            fontSize: 18,
                                            fontWeight: FontWeight.w500,
                                          ),
                                        )
                                      : null,
                                ),
                              ),
                            ),
                          ),
                        ),
                      ),
                      Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          if (model.autogenerated != null)
                            ...model.autogenerated!.specifications.map(
                              (specification) {
                                return Column(
                                  crossAxisAlignment: CrossAxisAlignment.start,
                                  children: [
                                    Padding(
                                      padding: const EdgeInsets.only(
                                          left: 18, right: 18),
                                      child: Row(
                                        children: [
                                          Text(
                                            specification.name.first,
                                            style: const TextStyle(
                                              fontSize: 16,
                                              fontWeight: FontWeight.bold,
                                            ),
                                          ),
                                          const SizedBox(width: 2),
                                          const Text(
                                            '*',
                                            style: TextStyle(
                                              fontSize: 16,
                                              fontWeight: FontWeight.bold,
                                              color: Colors.red,
                                            ),
                                          ),
                                        ],
                                      ),
                                    ),
                                    const Padding(
                                      padding:
                                          EdgeInsets.only(left: 18, right: 18),
                                      child: Text(
                                        'Choose 1',
                                        style: TextStyle(
                                          fontSize: 14,
                                          fontWeight: FontWeight.w300,
                                          color: Colors.grey,
                                        ),
                                      ),
                                    ),
                                    Column(
                                      crossAxisAlignment:
                                          CrossAxisAlignment.start,
                                      children: [
                                        ...specification.list.map((list) {
                                          int? index =
                                              specification.list.indexOf(list);
                                          return Column(
                                            crossAxisAlignment:
                                                CrossAxisAlignment.start,
                                            children: [
                                              if (specification.name.first ==
                                                  'Apartment Size')
                                                Row(
                                                  children: [
